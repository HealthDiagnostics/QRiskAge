        int qriskAge(int sex, int ethrisk, double score) 
        {
	        int i;
	        for (i = minAge; i <= maxAge; i++) 
		        if (referenceValuesGet_score(sex, i, ethrisk) > score)
			        break;
	        
            // if over the value of an 84 year old, return -1
	        if (referenceValuesGet_score(sex, maxAge, ethrisk) < score)
		        i= -1;

	        return i;
        }

